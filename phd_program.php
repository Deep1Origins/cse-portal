<div id="inner_content">	
Discipline of Computer Science and Engineering, IIT Indore offers Doctor of Philosophy (PhD) program in almost all current areas of Computer Science. 
It welcomes and accepts applications for admission to PhD program throughout the year. It is looking for highly motivated and dedicated research scholars for its PhD program. In general, students selected for PhD program have to undergo course-work during first two semesters before actually starting their individual doctoral research work. During these two semesters, students get an opportunity to acquaint themselves with the research activities of faculty members and suitably select their individual research supervisor.

<h2>Areas of Research</h2>
Faculty members of the discipline of Computer Science and Engineering are working in cutting-edge research areas. Complete list of areas in which currently active rearch is beining carried out can be found <a href = "research_area.html">here</a>. Information about faculty members and their research areas can be found <a href = "faculty.html">here</a>.

<h2> Admission Procedure </h2>
Selection process for admission to PhD program in discipline of Computer Science and Engineering consists of a written test followed by an interview. Syllabus of written test loosely follows the latest syllabus of Graduate Aptitude Test in Engineering (GATE) for Computer Science and Information Technology. Final selections are made considering performance of candidates in both, the written test and the interview.

<h2> Applications</h2>
IIT Indore invites applications for admission to its Regular PhD Program in every quarter of the year. For more details, please refer <a href = "http://academic.iiti.ac.in:8080/nregistration.jsp">here</a>.
